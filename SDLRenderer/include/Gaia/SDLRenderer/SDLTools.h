#include "Gaia/Color.h"

#include <SDL/SDL.h>

namespace gaia
{
	namespace SDLTools
	{
		///////////////////////////////////////////////////////////////////////////////
		/// \brief Converts an SDL color to a GUI color.
		///
		///	\return A GUI color equivalent to the SDL Color.
		///////////////////////////////////////////////////////////////////////////////
		inline Color toGuiColor(const ::SDL_Color& col)
		{
			return Color(col.r, col.g, col.b);
		}

		///////////////////////////////////////////////////////////////////////////////
		/// \brief Converts an GUI color to an SDL color.
		///
		///	\return A SDL color equivalent to the  GUI color.
		///////////////////////////////////////////////////////////////////////////////
		inline SDL_Color fromGuiColor(const Color& col)
		{
			SDL_Color sdlCol = {col.r, col.g, col.b, col.a};

			return sdlCol;
		}
	}
}
#endif